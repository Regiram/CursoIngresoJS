function mostrar()
{
	var mesAño
	mesAño = document.getElementById("txtIdMes").value;

	switch (mesAño){
		case "Enero":
			alert ("Que tengas un buen inicio de año");
			break;
		case "Marzo":
			alert ("A clases!!");
			break;
		case "Julio":
			alert("Se vienen las vacaciones!");
			break;
		case "Diciembre":
			alert("Felices Fiestas!!");
			break;
	}

}//FIN DE LA FUNCIÓN